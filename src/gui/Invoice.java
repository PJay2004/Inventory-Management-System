    public void printInvoice() {
        try {

            Date d = new Date();
            SimpleDateFormat s = new SimpleDateFormat("dd-MM-yyyy");
            String date = s.format(d);

            String cname = jLabel6.getText();
            String cno = jLabel7.getText();
            String city = jLabel8.getText();
            String pmethod = jComboBox1.getSelectedItem().toString();
            String total = jLabel20.getText();
            String paid = jTextField3.getText();
            String balance = jLabel24.getText();

            InputStream stream = Invoice.class.getResourceAsStream("Invoice1.jrxml");
            JasperReport jr = JasperCompileManager.compileReport(stream);

            HashMap parameters = new HashMap();

            parameters.put("date", date);
            parameters.put("invoiceid", uniqueId);
            parameters.put("cname", cname);
            parameters.put("cno", cno);
            parameters.put("ccity", city);
            parameters.put("pmethod", pmethod);
            parameters.put("paid", paid);
            parameters.put("balance", balance);
            parameters.put("total", total);

            TableModel tm = jTable1.getModel();
            JRTableModelDataSource dataSource = new JRTableModelDataSource(tm);

            JasperPrint jp = JasperFillManager.fillReport(jr, parameters, dataSource);
//            JasperViewer.viewReport(jp, false);
            JasperViewer jasperViewer = new JasperViewer(jp, false);
            jasperViewer.setAlwaysOnTop(true);
            jasperViewer.setVisible(true);
            
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

