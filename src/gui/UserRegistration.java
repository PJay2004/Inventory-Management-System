    public void loadUsers() {
        try {
            ResultSet rs = MySQL.search("SELECT `user`.`id`, `user`.`name`, `user`.`username`,`user`.`password`,`user`.`contact_number`,`city`.`name` AS `city_name` ,`user_status`.`name` AS `status_name` ,`user_type`.`name` AS `type_name` FROM `user` INNER JOIN `city` ON `city`.`id`= `user`.`city_id` INNER JOIN `user_type` ON `user_type`.`id`= `user`.`user_type_id` INNER JOIN `user_status` ON `user_status`.`id` = `user`.`user_status_id`ORDER BY `user`.`id` ASC;");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("username"));
                v.add(rs.getString("password"));
                v.add(rs.getString("contact_number"));
                v.add(rs.getString("type_name"));
                v.add(rs.getString("status_name"));
                v.add(rs.getString("city_name"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

